num = int(input("Введите число: "))

if num % 2 == 0:
    print("Число", num, "является чётным.")
else:
    print("Число", num, "является нечётным.")

if num % 3 == 0:
    print("Число делится на 3.")
else:
    print("Число не делится на 3.")
if num % 5 == 0:
    print("Число делится на 5.")
else:
    print("Число не делится на 5.")